int divi[6000000];

class PreprimeNumbers {
public:
	int nthNumber(int n) {
	    int cnt=0,i=1,j;
	    memset(divi,0,sizeof divi);
	    while(cnt<n){
	        i++;
            if(divi[i]>2)
                continue;
	        if(divi[i]==2)
                cnt++;
            if(cnt==n)
                return i;
            for(j=2*i;j<6000000;j+=i)
                divi[j]++;
	    }
	}
};


